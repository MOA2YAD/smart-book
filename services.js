const applyTranslations = (lang) => {
  const map = translations[lang] || translations.en;
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    if(!key) return;
    const val = map[key];
    if(val!==undefined){
      if(/<[^>]+>/.test(val)) el.innerHTML = val; else el.textContent = val;
    }
  });
  document.documentElement.lang = lang;
  if(lang === 'ar') document.documentElement.setAttribute('dir','rtl'); else document.documentElement.setAttribute('dir','ltr');
};

const selector = document.getElementById('lang');
const saved = localStorage.getItem('smartbook.lang') || navigator.language?.slice(0,2) || 'en';
selector.value = ['en','ar','fr','es'].includes(saved)? saved : 'en';
applyTranslations(selector.value);

selector.addEventListener('change', (e)=>{
  const v = e.target.value;
  localStorage.setItem('smartbook.lang', v);
  applyTranslations(v);
});
